def 지원금계산(연소득, 가구수):
    # 가구원 점수 (최대 5인 기준)
    점수 = [0, 10, 20, 30, 40][min(가구수, 5) - 1]

    # 소득 점수
    for 기준, 감점 in zip(
        [20000000, 40000000, 60000000, 80000000, float("inf")],
        [40, 30, 20, 10, 0]
    ):
        if 연소득 <= 기준:
            점수 += 감점
            break

    # 점수에 따른 지원금 분류
    for 기준점수, 금액 in zip(
        [80, 60, 40, 0],
        [1000000, 700000, 400000, 0]
    ):
        if 점수 >= 기준점수:
            return 점수, 금액


# ================= 메인 코드 =================

기준소득표 = [0, 2392013, 3932658, 5025353, 6097773, 7108192, 8064805, 8988428]
추가인원증가액 = 923623

국가부채기준 = int(input("현재 국가부채 기준 비율 입력: "))
국가부채비율 = int(input("현재 국가 부채 비율 입력: "))
가구원수 = int(input("가구원 수 입력: "))
소득 = int(input("연소득 입력: "))

지원금 = 0
점수 = "해당 없음"
기준소득 = "해당 없음"
판단사유 = ""

# 1️⃣ 국가 부채 판단
if 국가부채비율 > 국가부채기준:
    판단사유 = "국가 부채 비율 초과"

else:
    # 2️⃣ 기준 소득 계산
    if 가구원수 <= 7:
        기준소득 = 기준소득표[가구원수]
    else:
        기준소득 = 기준소득표[7] + (가구원수 - 7) * 추가인원증가액

    # 3️⃣ 점수 및 지원금 계산
    점수, 계산된지원금 = 지원금계산(소득, 가구원수)

    if 소득 <= 기준소득:
        지원금 = 계산된지원금
        판단사유 = "소득 기준 충족"
    else:
        판단사유 = "소득 초과"

# 4️⃣ 출력
print("\n=== 결과 ===")
print("기준 소득:", 기준소득)
print("점수:", 점수)
print("지원금:", 지원금)
print("판단 사유:", 판단사유)
